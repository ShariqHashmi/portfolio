                                data-srcset="" sizes="(max-width: 800px) 100vw, 800px"></p>
                        <p>Moreover, these crypto hackers are so intelligent that they may analyze your activities;
                            whether you used email app frequently or love to go on new websites on a regular basis.
                            In that way, they figure it out how they can maximize their return by using your system
                            accordingly</p>
                        <h3>3.&nbsp; Hackers uses Browsing Mining Technique for Cryptojacking Purposes</h3>
                        <p>The reason why browsing mining is one of the best technique for hackers to do
                            Cryptojacking is that it doesn’t require any program to be installed for secretly mining
                            cryptocurrency.</p>
                        <p>The browser mining can be done by writing a Cryptojacking script such as Javascript that
                            enables the hackers to mine different cryptocurrencies due to its ability to use a part
                            of victim’s system processing unit without letting them know.</p>
                        <p>According to Investopedia, the official incident of Cryptojacking comes into notice when
                            a cybersecurity company “Panda,” wrote a Cryptojacking script by the name of “WannaMine”
                            had rolled out to different systems randomly. This Cryptojacking script is used for
                            mining of specific cryptocurrency known as “Monero.”</p>
                        <p><img class="img-fluid aligncenter wp-image-1549 size-full lazy-loaded"
                                title="monero script"
